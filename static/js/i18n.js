function switchLanguage(lang) {
    if (!translations[lang]) return;

    // Save preference
    localStorage.setItem("preferred_language", lang);

    // Update select value
    const langSelects = document.querySelectorAll(".lang-switcher");
    langSelects.forEach(select => select.value = lang);

    // Update text elements
    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.getAttribute("data-i18n");
        if (translations[lang][key]) {
            // Check if element is an input with placeholder
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translations[lang][key];
            } else if (el.tagName === 'OPTION') { // Note: OPTION tags handled below but just in case
                el.textContent = translations[lang][key];
            } else {
                // If the element has child elements, we might lose them if we just set textContent.
                // However, most of our elements with data-i18n contain only text or we separate the spans.
                // It's best if HTML tags inside are avoided where data-i18n is used.
                // Since some elements (like buttons with spans) have icons, we shouldn't overwrite the whole innerHTML.
                // Let's assume if it has data-i18n, its primary content is text.
                el.textContent = translations[lang][key];
            }
        }
    });

    // Also handle elements with child elements correctly if needed (workaround for buttons with emojis)
    document.querySelectorAll("[data-i18n-html]").forEach(el => {
        const key = el.getAttribute("data-i18n-html");
        if (translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        }
    });

    // Handle RTL
    if (lang === "ar") {
        document.body.setAttribute("dir", "rtl");
        document.body.classList.add("rtl-lang");
    } else {
        document.body.setAttribute("dir", "ltr");
        document.body.classList.remove("rtl-lang");
    }
}

// Auto-initialize language on page load
document.addEventListener("DOMContentLoaded", () => {
    const savedLang = localStorage.getItem("preferred_language") || "en";

    // Add event listener to all switchers
    const langSelects = document.querySelectorAll(".lang-switcher");
    langSelects.forEach(langSelect => {
        langSelect.addEventListener("change", (e) => {
            switchLanguage(e.target.value);
        });
    });

    switchLanguage(savedLang);
});
